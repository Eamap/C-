#include<iostream>
using namespace std;

int main(){
    // we use \n for new line
    cout << "Eslam \n Omar" << endl;

    // we use \b for backSpace
    cout << "Eslam\b Omar" << endl;

    // we use \a for make sound
    cout << "Eslam\a Omar" << endl;

    // we use \b for make tab
    cout << "Eslam\t Omar" << endl;

    // we use \r for new line
    cout << "Eslam\r Omar" << endl;
    return 0;
}
